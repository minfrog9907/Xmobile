public class
ShortCutItem {
    String filename;
    String path;
    long size;
    String userid;
    String displayName;
    short type;

    public long getSize() {
        return size;
    }

    public String getFilename() {
        return filename;
    }

    public String getPath() {
        return path;
    }

    public String getUserid() {
        return userid;
    }

    public short getType() {
        return type;
    }

    public String getDisplayName() {
        return displayName;
    }
}
